/**
 * Deterministic name picker. Uses index-based selection to ensure
 * reproducible data across seed runs.
 */
export function pickMaleName(index: number): string {
  return MALE_FIRST_NAMES[index % MALE_FIRST_NAMES.length]!;
}

export function pickFemaleName(index: number): string {
  return FEMALE_FIRST_NAMES[index % FEMALE_FIRST_NAMES.length]!;
}

export function pickFamilyName(index: number): string {
  return FAMILY_NAMES[index % FAMILY_NAMES.length]!;
}

/**
 * Generate a deterministic email from a name and index.
 */
export function makeEmail(firstName: string, lastName: string, index: number, domain: string): string {
  const clean = (s: string) => s.toLowerCase().replace(/[^a-z]/g, '');
  return `${clean(firstName)}.${clean(lastName)}${index > 0 ? index : ''}@${domain}`;
}

